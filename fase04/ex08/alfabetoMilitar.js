function alfabetoMilitar(n) {
    if (n=="a" || n=="A")
    return "alpha"
    else if (n=="e" || n=="E")
    return "echo"
    else if (n=="i" || n=="I")
    return "india"
    else if (n=="o" || n=="O")
    return "oscar"
    else if (n=="u" || n=="U")
    return "uniform"
    else
    return "false"
}
